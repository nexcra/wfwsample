/******************************************************************************
* 카드정보.
* @author 이은호
* @version 2005-08-04 
******************************************************************************/
public class CardUseInfoEtt implements java.io.Serializable 
{
	private String bankNo;			// 회원사번호
	private String cardNo;			// 카드번호
	private String cardType;		// 카드구분 1:본인 2:가족 3:지정 4:공용
	private String bankName;		//회원사명
	private String joinNo;			//제휴업체번호번호
	private String joinName;		//제휴카드명
	private String brand;			//브랜드(B-BC,M-마스터,V-비자,J-JVC)
	private String vipType;			//0:일반, 1:PT12(일반PT),2:PT03(e-PT),3:PT30(다이야몬드), 4:PT91(인피니티)
	private String transport;		//교통카드후불여부(0-미대상, 1-대상)
	private String cardCode;		//카드 상품코드, Appended By PWT 20070901
	private String acctDay;			//결제일, Appended By PWT 20070920
	private String bankType;		//독자은행 여부 0-비독자, 1-독자, Appended By PWT 20070920
	
	/** ****************************************************************************
	 * 은행번호
	 * @param 
	 **************************************************************************** */
	public void setBankNo(String bankNo)			{	this.bankNo = bankNo;			}

	/** ****************************************************************************
	 * 카드번호
	 * @param 
	 **************************************************************************** */
	public void setCardNo(String cardNo)			{	this.cardNo = cardNo;			}

	/** ****************************************************************************
	 * 카드타입
	 * @param 
	 **************************************************************************** */
	public void setCardType(String cardType)		{	this.cardType = cardType;		}

	/** ****************************************************************************
	 * 은행명
	 * @param 
	 **************************************************************************** */
	public void setBankName(String bankName)		{	this.bankName = bankName;		}

	/** ****************************************************************************
	 * 제휴번호
	 * @param 
	 **************************************************************************** */
	public void setJoinNo(String joinNo)			{	this.joinNo = joinNo;			}

	/** ****************************************************************************
	 * 제휴명
	 * @param 
	 **************************************************************************** */
	public void setJoinName(String joinName)		{	this.joinName = joinName;		}
	/** ****************************************************************************
	 * 브랜드
	 * @param 
	 **************************************************************************** */
	public void setBrand(String brand)				{	this.brand = brand;				}
	/** ****************************************************************************
	 * VIP타입
	 * @param 
	 **************************************************************************** */
	public void setVipType(String vipType)			{	this.vipType = vipType;			}
	/** ****************************************************************************
	 * 교통카드여부
	 * @param 
	 **************************************************************************** */
	public void setTransport(String transport)		{	this.transport = transport;		}
	/** ****************************************************************************
	 * 카드상품코드
	 * @param 
	 **************************************************************************** */
	public void setCardCode(String cardCode)		{	this.cardCode = cardCode;		}
	/** ****************************************************************************
	 * 독자은행여부
	 * @param 
	 **************************************************************************** */
	public void setBankType(String bankType)		{	this.bankType = bankType;		}
	/** ****************************************************************************
	 * 결제일
	 * @param 
	 **************************************************************************** */
	public void setAcctDay(String acctDay)		{	this.acctDay = acctDay;		}



	/** ****************************************************************************
	 * 은행코드
	 * @return 
	 **************************************************************************** */
	public String getBankNo()		{	return bankNo;		}
	/** ****************************************************************************
	 * 카드번호
	 * @return 
	 **************************************************************************** */
	public String getCardNo()		{	return cardNo;		}
	/** ****************************************************************************
	 * 카드타입
	 * @return 
	 **************************************************************************** */
	public String getCardType()		{	return cardType;	}
	/** ****************************************************************************
	 * 은행명
	 * @return 
	 **************************************************************************** */
	public String getBankName()		{	return bankName;	}
	/** ****************************************************************************
	 * 제휴번호
	 * @return 
	 **************************************************************************** */
	public String getJoinNo()		{	return joinNo;		}
	/** ****************************************************************************
	 * 제휴명
	 * @return 
	 **************************************************************************** */
	public String getJoinName()		{	return joinName;	}
	/** ****************************************************************************
	 * 브랜드명
	 * @return 
	 **************************************************************************** */
	public String getBrand()		{	return brand;		}
	/** ****************************************************************************
	 * VIP타입
	 * @return 
	 **************************************************************************** */
	public String getVipType()		{	return vipType;		}
	/** ****************************************************************************
	 * 교통카드여부
	 * @return 
	 **************************************************************************** */
	public String getTransport()	{	return transport;	}
	/** ****************************************************************************
	 * 상품코드
	 * @return 
	 **************************************************************************** */
	public String getCardCode()		{	return cardCode;	}

	/** ****************************************************************************
	 * 독자은행여부
	 * @return 
	 **************************************************************************** */
	public String getBankType()		{	return bankType;	}
	/** ****************************************************************************
	 * 결제일
	 * @return 
	 **************************************************************************** */
	public String getAcctDay()		{	return acctDay;	}
	/** ****************************************************************************
	 * 카드매스크
	 * @return 
	 **************************************************************************** */
	public String getMaskedCardNo() {
		return cardNo.substring(0,4) + "-****-****-" + cardNo.substring(12) + "("+bankName+")";
	}
}